// Emit a JS array literal block for processed events that can be pasted into events.js
import fs from 'node:fs';

const list = JSON.parse(fs.readFileSync('scripts/processed-events.json', 'utf8'));

// Centroid for Mumbai by venue keyword (rough)
const VENUE_COORDS = [
  [/embassy tech village|bellandur/i, 12.9352, 77.6907],
  [/whitefield/i, 12.9698, 77.7500],
  [/koramangala/i, 12.9352, 77.6245],
  [/indiranagar/i, 12.9784, 77.6408],
  [/jayanagar/i, 12.9250, 77.5938],
  [/electronic city/i, 12.8456, 77.6603],
  [/marathahalli/i, 12.9569, 77.7011],
  [/hebbal/i, 13.0359, 77.5970],
  [/yelahanka/i, 13.1007, 77.5963],
  [/sarjapur/i, 12.9009, 77.6878],
  [/hsr layout|hsr/i, 12.9116, 77.6473],
  [/btm/i, 12.9166, 77.6101],
  [/manyata|hebbal/i, 13.0438, 77.6206],
  [/iisc|tata audi/i, 13.0218, 77.5671],
  [/ktpo/i, 12.9863, 77.7371],
  [/biec/i, 13.0843, 77.4902],
  [/nimhans/i, 12.9430, 77.5942],
  [/garden city/i, 13.0827, 77.7042],
  [/church street/i, 12.9747, 77.6063],
  [/urban vault/i, 12.9716, 77.5946],
  [/microsoft/i, 12.9716, 77.5946],
];
const DEFAULT = [12.9716, 77.5946];

function coordsFor(venue) {
  const v = venue || '';
  for (const [rx, lat, lng] of VENUE_COORDS) if (rx.test(v)) return [lat, lng];
  return DEFAULT;
}

function js(s) { return JSON.stringify(s); }
function cleanUrl(u) {
  try {
    const x = new URL(u);
    // Strip tracking params for known sources
    if (/meetup\.com$/.test(x.host) || /eventbrite\./.test(x.host)) x.search = '';
    return x.toString();
  } catch { return u; }
}

const out = [];
out.push('  // === Auto-scraped: Apr 27 – May 30, 2026 ===');
out.push('  // Source: Eventbrite, Meetup, HasGeek, GDG. Generated by scripts/scrape-events.mjs');
for (const e of list) {
  const [lat, lng] = coordsFor(e.venue);
  out.push('  {');
  out.push(`    id: ${e.id},`);
  out.push(`    name: ${js(e.name)},`);
  out.push(`    date: ${js(e.date)},`);
  out.push(`    startDate: ${js(e.startDate)},`);
  out.push(`    endDate: ${js(e.endDate)},`);
  out.push(`    venue: ${js(e.venue)},`);
  out.push(`    time: ${js(e.time)},`);
  out.push(`    cost: ${js(e.cost)},`);
  out.push(`    category: ${js(e.category)},`);
  out.push(`    tags: ${JSON.stringify(e.tags)},`);
  out.push(`    description: ${js(e.description)},`);
  out.push(`    link: ${js(cleanUrl(e.link))},`);
  out.push(`    lat: ${lat},`);
  out.push(`    lng: ${lng},`);
  out.push(`    source: ${js(e.source)},`);
  out.push('  },');
}

const block = out.join('\n');
fs.writeFileSync('scripts/events-block.js', block);
console.log(`Wrote ${list.length} entries to scripts/events-block.js (${block.length} chars)`);
